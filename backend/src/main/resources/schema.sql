CREATE TABLE IF NOT EXISTS pet_care_records (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pet_id BIGINT NOT NULL REFERENCES pets(id),
    recorded_by_user_id BIGINT NOT NULL REFERENCES users(id),
    care_type VARCHAR(30) NOT NULL CHECK (care_type IN ('RABIES', 'HEARTWORM', 'COMBO_VACCINE', 'MEDICAL_VISIT', 'OTHER')),
    administered_on DATE NOT NULL,
    next_due_on DATE NOT NULL,
    memo VARCHAR(500),
    deleted_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS symptom_checks (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pet_id BIGINT NOT NULL REFERENCES pets(id),
    requested_by_user_id BIGINT NOT NULL REFERENCES users(id),
    symptom_type VARCHAR(100) NOT NULL,
    onset_text VARCHAR(100),
    memo VARCHAR(500),
    severity VARCHAR(10) NOT NULL CHECK (severity IN ('LOW', 'MEDIUM', 'HIGH')),
    recommendation VARCHAR(10) NOT NULL CHECK (recommendation IN ('OBSERVE', 'CONSULT', 'VISIT')),
    ai_model VARCHAR(100),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS appointments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pet_id BIGINT NOT NULL REFERENCES pets(id),
    owner_user_id BIGINT NOT NULL REFERENCES users(id),
    staff_user_id BIGINT REFERENCES users(id),
    appointment_type VARCHAR(20) NOT NULL CHECK (appointment_type IN ('CONSULTATION', 'MEDICAL')),
    channel VARCHAR(20) NOT NULL CHECK (channel IN ('ONLINE', 'VISIT', 'HOSPITAL')),
    scheduled_at TIMESTAMP NOT NULL,
    status VARCHAR(20) NOT NULL CHECK (status IN ('REQUESTED', 'CONFIRMED', 'CANCELED', 'COMPLETED')),
    zoom_join_url VARCHAR(500),
    note VARCHAR(500),
    deleted_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

ALTER TABLE appointments ADD COLUMN IF NOT EXISTS zoom_join_url VARCHAR(500);

CREATE TABLE IF NOT EXISTS medical_histories (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pet_id BIGINT NOT NULL REFERENCES pets(id),
    appointment_id BIGINT UNIQUE REFERENCES appointments(id),
    handled_by_user_id BIGINT NOT NULL REFERENCES users(id),
    performed_on DATE NOT NULL,
    treatment_detail VARCHAR(1000) NOT NULL,
    diagnosis VARCHAR(1000),
    prescription VARCHAR(1000),
    deleted_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS medical_attachments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    medical_history_id BIGINT NOT NULL REFERENCES medical_histories(id),
    file_name VARCHAR(255) NOT NULL,
    file_path VARCHAR(500) NOT NULL,
    file_mime_type VARCHAR(100) NOT NULL,
    file_size_bytes BIGINT NOT NULL,
    description VARCHAR(200),
    uploaded_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);


CREATE TABLE IF NOT EXISTS consult_chat_messages (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES users(id),
    sender_type VARCHAR(10) NOT NULL CHECK (sender_type IN ('USER', 'BOT')),
    message VARCHAR(1000) NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS pet_calendar_marks (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pet_id BIGINT NOT NULL REFERENCES pets(id),
    created_by_user_id BIGINT NOT NULL REFERENCES users(id),
    mark_date DATE NOT NULL,
    mark_type VARCHAR(20) NOT NULL CHECK (mark_type IN ('KARTE', 'VACCINE', 'HOSPITAL', 'TRIMMING', 'DOG_RUN', 'MEDICINE')),
    memo VARCHAR(500),
    deleted_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS plans (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    monthly_fee DECIMAL(10,2) NOT NULL,
    features_json JSONB,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    deleted_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS subscriptions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES users(id),
    pet_id BIGINT NOT NULL REFERENCES pets(id),
    plan_id BIGINT NOT NULL REFERENCES plans(id),
    start_date DATE NOT NULL,
    end_date DATE,
    status VARCHAR(20) NOT NULL CHECK (status IN ('ACTIVE', 'PAUSED', 'CANCELED')),
    auto_renew BOOLEAN NOT NULL DEFAULT TRUE,
    deleted_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS invoices (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    subscription_id BIGINT NOT NULL REFERENCES subscriptions(id),
    invoice_number VARCHAR(50) NOT NULL UNIQUE,
    invoice_date DATE NOT NULL,
    due_date DATE NOT NULL,
    amount DECIMAL(10,2) NOT NULL,
    payment_status VARCHAR(20) NOT NULL CHECK (payment_status IN ('UNPAID', 'PARTIAL', 'PAID')),
    issued_at TIMESTAMP,
    paid_at TIMESTAMP,
    deleted_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS payments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    invoice_id BIGINT NOT NULL REFERENCES invoices(id),
    paid_amount DECIMAL(10,2) NOT NULL,
    paid_at TIMESTAMP,
    payment_method VARCHAR(20) NOT NULL CHECK (payment_method IN ('CARD', 'BANK', 'OTHER')),
    transaction_ref VARCHAR(100),
    status VARCHAR(20) NOT NULL CHECK (status IN ('PENDING', 'SUCCEEDED', 'FAILED')),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS notifications (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    notification_type VARCHAR(20) NOT NULL CHECK (notification_type IN ('REMINDER', 'INFO', 'ALERT')),
    title VARCHAR(100) NOT NULL,
    body VARCHAR(2000) NOT NULL,
    scheduled_at TIMESTAMP,
    sent_at TIMESTAMP,
    delivery_status VARCHAR(20) NOT NULL CHECK (delivery_status IN ('DRAFT', 'SCHEDULED', 'SENT', 'FAILED')),
    created_by_user_id BIGINT NOT NULL REFERENCES users(id),
    deleted_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS notification_recipients (
    notification_id BIGINT NOT NULL REFERENCES notifications(id),
    user_id BIGINT NOT NULL REFERENCES users(id),
    read_at TIMESTAMP,
    delivery_status VARCHAR(20) NOT NULL CHECK (delivery_status IN ('PENDING', 'SENT', 'FAILED')),
    PRIMARY KEY (notification_id, user_id)
);

CREATE TABLE IF NOT EXISTS email_templates (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    template_code VARCHAR(100) NOT NULL UNIQUE,
    subject_template VARCHAR(200) NOT NULL,
    body_template TEXT NOT NULL,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS email_messages (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    template_id BIGINT NOT NULL REFERENCES email_templates(id),
    recipient_user_id BIGINT NOT NULL REFERENCES users(id),
    pet_id BIGINT REFERENCES pets(id),
    appointment_id BIGINT REFERENCES appointments(id),
    invoice_id BIGINT REFERENCES invoices(id),
    subject VARCHAR(200) NOT NULL,
    body TEXT NOT NULL,
    send_timing_at TIMESTAMP,
    status VARCHAR(20) NOT NULL CHECK (status IN ('QUEUED', 'SENT', 'FAILED')),
    error_message TEXT,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    sent_at TIMESTAMP
);

